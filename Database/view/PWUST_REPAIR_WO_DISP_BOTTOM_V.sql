CREATE OR REPLACE VIEW PWUST_REPAIR_WO_DISP_BOTTOM_V AS
SELECT T.ORDER_ID,
       T.ORDER_NO,
       T.PLAN_NO,
       T.PART_NO,
       T.PLAN_REVISION,
       T.PLAN_TITLE,
       T.ALT_COUNT,
       T.ORDER_STATUS,
       T.ALT_STATUS,
       T.ORDER_HOLD_STATUS,
       (SELECT T1.UPDT_USERID FROM SFWID_ALTERATION_DESC T1 WHERE T1.ALT_ID = T.ALT_ID) AS ALTERED_BY,
       (SELECT T1.ALT_REASON  FROM SFWID_ALTERATION_DESC T1 WHERE T1.ALT_ID = T.ALT_ID) AS REASON_FOR_CHANGE,
       T.UCF_ORDER_DATE3 AS RELEASE_DATE,
       T2.SALES_ORDER,
       T.SECURITY_GROUP
FROM SFWID_ORDER_DESC T, PWUST_SFWID_ORDER_DESC T2
WHERE T.ORDER_ID = T2.ORDER_ID
AND T2.PW_ORDER_TYPE IN ('REPAIR');
/
grant select on PWUST_REPAIR_WO_DISP_BOTTOM_V to pratt_read_only ;
grant select on PWUST_REPAIR_WO_DISP_BOTTOM_V to sf$connection_pool;