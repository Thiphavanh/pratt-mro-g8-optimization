CREATE OR REPLACE VIEW SFMFG.PWUE_SFQA_DISC_TASK_DISPATCH_V AS
select order_no,
       OPER_NO,
       DISC_ID,
       DISC_LINE_NO,
       DISC_LINE_TITLE,
       DISC_LINE_STATUS,
       ITEM_ID,
       AFFECTED_QTY,
       ROUTE_TYPE,
       DISP_TYPE,
       CUST_NOTIF_FLAG,
       DISP_INSTR_TYPE,
       DISP_ORDER_ID,
       DISP_DOC_TYPE,
       DISP_DOC_NO,
       REJECT_WORK_LOC,
       REJECT_WORK_DEPT,
       REJECT_WORK_CENTER,
       NOTES,
       PO_LINE_ITEM,
       REJECT_PART_NO,
       CA_FLAG,
       CA_ID,
       MERGED_DISP_DOC_TYPE,
       MERGED_DISP_DOC_NO,
       QUEUE_TYPE,
       TASK_STATUS,
       TASK_ID,
       UPDT_USERID,
       TIME_STAMP,
       SECURITY_GROUP,
       UNIT_NO              AS END_UNIT_NO,
       UNIT_TYPE            AS END_UNIT_TYPE
  from SFMFG.PWUE_SFQA_DISC_TASK_DISPATCH_V v
 where (QUEUE_TYPE like 'ENG%' or QUEUE_TYPE = 'ME_APPROVAL')
   and TASK_STATUS in ('IN QUEUE', 'ACTIVE');

GRANT SELECT ON SFMFG.PWUE_SFQA_DISC_TASK_DISPATCH_V TO PRATT_READ_ONLY;
grant select, insert, update, delete on SFMFG.PWUE_SFQA_DISC_TASK_DISPATCH_V to SF$CONNECTION_POOL;
